    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed
        if (!dataLengkap()) {
            JOptionPane.showMessageDialog(null, "Silahkan Lengkapi Datanya !");
        } else {
            String email = textEmail.getText();
            String name = textName.getText();
            String nomorHP = textNomorHP.getText();
            String alamat = textAlamat.getText();
            String tanggalLahir = textTanggalLahir.getText();
            String id_cabang = comboboxIDCabang.getSelectedItem().toString();
            String jabatan = comboboxJabatan.getSelectedItem().toString();
            
            String query_profile = String.format("insert into karyawan  (nama, tanggal_lahir, nomor_hp, email, alamat, id_cabang, jabatan) " +
                    "values ('%s', '%s', '%s', '%s', '%s', '%s', '%s');",
                    name, tanggalLahir, nomorHP, alamat, email, id_cabang, jabatan);
            
            ResultSet rs;
            
            try {
                Statement statement = connection.createStatement();
                System.out.println("Trying to query : " + query_profile);
                statement.executeUpdate(query_profile);
                System.out.println("Berhasil mendaftar karyawan");
                resetForm();
                refresh();
                JOptionPane.showMessageDialog(null, "Berhasil mendaftar karyawan");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Ada error ! Silahkan cek datanya terutama email dan tanggal lahir");
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

